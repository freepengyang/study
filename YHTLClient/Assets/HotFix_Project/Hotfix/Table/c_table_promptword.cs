	public static class PROMPTWORDHelper
	{
		public static void Encode(this System.IO.Stream stream,PROMPTWORD item)
		{
			stream.Encode(item.id);
			stream.Encode(item.dec);
			stream.Encode(item.title);
			stream.Encode(item.leftBtn);
			stream.Encode(item.rightBtn);
			stream.Encode(item.toggle);
		}
