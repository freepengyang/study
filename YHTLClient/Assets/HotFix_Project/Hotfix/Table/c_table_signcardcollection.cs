	public class SIGNCARDCOLLECTIONARRAY  : ILFastMode
	{
		public SIGNCARDCOLLECTIONARRAY()
		{
			LongValueFixedLength = 0;
			IntValueFixedLength = 3;
			StringValueFixedLength = 3;
			Rules = new byte[]{1,2,1,2,1,2};
		}
		public override void Decode(byte[] contents)
		{
			gItem = GDecoder.LoadTable(contents, this.Rules);
			this.VarIntValues = GDecoder.varIntValues;
			this.VarStringValues = GDecoder.varStringValues;
			this.VarLongValues = GDecoder.varLongValues;
			var handles = gItem.handles;
			TableHandle handle = null;
			for (int i = 0, max = handles.Length; i < max; ++i)
			{
				TABLE.SIGNCARDCOLLECTION randAttr = new TABLE.SIGNCARDCOLLECTION();
				handle = handles[i];
				randAttr.__data = handle.data;
				randAttr.Array = this;
				handle.Value = randAttr;
				randAttr.handle = handle;
			}
		}
	}
