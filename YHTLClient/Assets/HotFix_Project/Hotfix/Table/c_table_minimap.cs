	public static class MINIMAPHelper
	{
		public static void Encode(this System.IO.Stream stream,MINIMAP item)
		{
			stream.Encode(item.id);
			stream.Encode(item.mid);
			stream.Encode(item.x);
			stream.Encode(item.y);
			stream.Encode(item.type);
			stream.Encode(item.coordOffsetPtr);
			stream.Encode(item.name);
